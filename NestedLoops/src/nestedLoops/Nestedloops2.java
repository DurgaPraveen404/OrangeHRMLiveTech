package nestedLoops;

import java.util.Scanner;

public class Nestedloops2 {

	public static void main(String[] args) {
		Scanner s=new Scanner(System.in);
		int a=s.nextInt();
		int i,j;
		for(i=1;i<=a;i++) {
			for(j=1;j<=2-1;j++) {
				System.out.print("*");
			}
			System.out.println();
		}

	}

}
